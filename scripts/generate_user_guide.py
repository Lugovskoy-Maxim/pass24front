#!/usr/bin/env python3
"""Generate user instruction document for PASS24 / M-STYLE portal."""

from pathlib import Path

from docx import Document
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.shared import Pt, Cm


def add_heading(doc: Document, text: str, level: int = 1) -> None:
    doc.add_heading(text, level=level)


def add_paragraph(doc: Document, text: str, bold: bool = False) -> None:
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = bold
    run.font.size = Pt(11)


def add_bullets(doc: Document, items: list[str]) -> None:
    for item in items:
        doc.add_paragraph(item, style='List Bullet')


def add_numbered(doc: Document, items: list[str]) -> None:
    for item in items:
        doc.add_paragraph(item, style='List Number')


def build_document() -> Document:
    doc = Document()

    section = doc.sections[0]
    section.top_margin = Cm(2)
    section.bottom_margin = Cm(2)
    section.left_margin = Cm(2.5)
    section.right_margin = Cm(2)

    title = doc.add_paragraph()
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = title.add_run('Инструкция пользователя\nСистема заказа пропусков')
    run.bold = True
    run.font.size = Pt(18)

    subtitle = doc.add_paragraph()
    subtitle.alignment = WD_ALIGN_PARAGRAPH.CENTER
    sub_run = subtitle.add_run('M-STYLE / PASS24')
    sub_run.font.size = Pt(12)

    doc.add_paragraph()

    add_heading(doc, '1. Назначение системы', 1)
    add_paragraph(
        doc,
        'Портал предназначен для заказа гостевых пропусков арендаторами бизнес-центра, '
        'обработки заявок на ресепшене и администрирования пользователей. '
        'Работает в браузере на компьютере и телефоне; может быть установлен как приложение (PWA).',
    )

    add_heading(doc, '2. Вход и регистрация', 1)
    add_heading(doc, '2.1. Вход в систему', 2)
    add_paragraph(doc, 'На странице входа укажите:')
    add_bullets(doc, [
        'Логин, email или номер телефона в формате +7 (999) 000-00-00',
        'Пароль',
    ])
    add_paragraph(doc, 'После успешного входа откроется главная страница в соответствии с вашей ролью.')

    add_heading(doc, '2.2. Регистрация арендатора', 2)
    add_paragraph(doc, 'Новые арендаторы регистрируются на той же странице, вкладка «Регистрация»:')
    add_numbered(doc, [
        'Заполните ФИО, название компании и пароль (не менее 6 символов).',
        'Выберите способ подтверждения: «По email» или «По SMS».',
        'Укажите email и/или телефон в зависимости от выбранного способа.',
        'Нажмите «Получить код» — на email или телефон придёт 6-значный код (действует 15 минут).',
        'Введите код и нажмите «Подтвердить регистрацию».',
    ])
    add_paragraph(
        doc,
        'После регистрации заявка ожидает подтверждения администратором. '
        'До активации аккаунта вверху страницы отображается уведомление о том, что доступ будет открыт после одобрения.',
    )
    add_paragraph(
        doc,
        'Если регистрация по SMS временно отключена администратором, при нажатии на вкладку «По SMS» '
        'появится информационное сообщение. В этом случае используйте подтверждение по email.',
    )

    add_heading(doc, '3. Роли пользователей', 1)
    add_bullets(doc, [
        'Арендатор — заказывает пропуска для посетителей и курьеров, просматривает свои заявки.',
        'Сотрудник компании — действует от имени арендатора в рамках выданных прав.',
        'Ресепшн / охрана — журнал посетителей, пропуск на вход и выход, поиск пропусков.',
        'Администратор БЦ — управление офисами, пользователями, настройками.',
        'Супер-администратор — полный доступ, включая настройки сайта и регистрации по SMS.',
    ])

    add_heading(doc, '4. Заказ пропуска (арендатор)', 1)
    add_numbered(doc, [
        'Перейдите в раздел «Пропуска» → «Заказать пропуск».',
        'Выберите тип пропуска (гость, курьер и др., если доступны для вашей роли).',
        'Укажите данные посетителя: ФИО, телефон, дату и время визита, офис назначения.',
        'При необходимости заполните паспортные данные или номер автомобиля.',
        'Отправьте заявку.',
    ])
    add_paragraph(
        doc,
        'Статусы заявки: на рассмотрении → одобрен → активен (посетитель в здании) → завершён. '
        'До одобрения заявку можно отменить. Одобренный пропуск можно распечатать или отправить ссылку посетителю.',
    )

    add_heading(doc, '5. Список пропусков', 1)
    add_paragraph(doc, 'В разделе «Пропуска» отображаются ваши или все заявки (в зависимости от прав):')
    add_bullets(doc, [
        'Поиск по ФИО, телефону, офису, номеру пропуска',
        'Фильтр по дате и статусу',
        'Показ 50 последних записей; кнопка «Ещё» загружает следующие',
        'Клик по строке открывает детали пропуска',
        'Экспорт в CSV и отчёт по пропускам — через соответствующие кнопки',
    ])
    add_paragraph(doc, 'Список автоматически обновляется каждые 20 секунд.')

    add_heading(doc, '6. Ресепшн (панель приёма)', 1)
    add_paragraph(doc, 'Раздел «Ресепшн» — журнал посетителей на выбранную дату.')
    add_heading(doc, '6.1. Поиск', 2)
    add_paragraph(doc, 'В строке поиска можно ввести номер пропуска, ФИО, телефон, паспорт или офис. Результаты фильтруются в журнале за выбранный день.')
    add_heading(doc, '6.2. Работа с посетителями', 2)
    add_bullets(doc, [
        'Ожидают входа — кнопка «Пропустить на вход» или «Отклонить» (с указанием причины)',
        'В здании — кнопка «Зафиксировать выход»',
        'Просроченные гости — отдельный блок с предупреждением',
    ])
    add_paragraph(doc, 'Справа (на большом экране) отображаются детали выбранного пропуска.')

    add_heading(doc, '7. Профиль', 1)
    add_paragraph(
        doc,
        'В профиле арендатор может просмотреть свои данные. '
        'Изменение профиля после регистрации может требовать согласования администратора. '
        'Владелец компании может добавлять сотрудников с ограниченными правами.',
    )

    add_heading(doc, '8. Шаблоны пропусков', 1)
    add_paragraph(
        doc,
        'Шаблоны сохраняют часто используемые данные посетителей для быстрого повторного заказа. '
        'Создайте шаблон при заказе пропуска или в разделе «Шаблоны».',
    )

    add_heading(doc, '9. Пропуск для посетителя (QR-код)', 1)
    add_paragraph(
        doc,
        'Посетитель получает ссылку на электронный пропуск с QR-кодом. '
        'Сотрудник ресепшена может найти пропуск по номеру или данным посетителя и оформить вход.',
    )

    add_heading(doc, '10. Частые вопросы', 1)
    add_bullets(doc, [
        'Не пришёл код на email — проверьте папку «Спам», запросите код повторно.',
        'Не пришло SMS — убедитесь, что номер указан как +7 (9XX) XXX-XX-XX; при отключённой SMS-регистрации используйте email.',
        'Не могу войти после регистрации — дождитесь активации аккаунта администратором.',
        'Не вижу раздел — доступ зависит от роли; обратитесь к администратору БЦ.',
    ])

    add_heading(doc, '11. Контакты поддержки', 1)
    add_paragraph(
        doc,
        'Контактные телефон и email указаны на странице входа и в шапке сайта. '
        'По вопросам доступа и офисов обращайтесь к администратору бизнес-центра.',
    )

    footer = doc.add_paragraph()
    footer.alignment = WD_ALIGN_PARAGRAPH.CENTER
    footer_run = footer.add_run('Документ сгенерирован автоматически. Версия системы: 2026.')
    footer_run.font.size = Pt(9)
    footer_run.italic = True

    return doc


def main() -> None:
    root = Path(__file__).resolve().parents[1]
    out_dir = root / 'docs'
    out_dir.mkdir(exist_ok=True)
    out_path = out_dir / 'Инструкция_пользователя.docx'
    doc = build_document()
    doc.save(out_path)
    print(f'Created: {out_path}')


if __name__ == '__main__':
    main()